#include <bits/stdc++.h>
using namespace std;

int t, m, n;
char board[11][11];


int main(void){
    ios::sync_with_stdio(false); cin.tie(NULL); cout.tie(NULL);
    
    cin>>t;
    while(t--){
        cin>>n>>m;
        for(int i=0; i<n; i++){
            for(int j=0; j<m; j++) cin>>board[i][j];
        }

        
    }
}