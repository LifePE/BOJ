import sys
input=sys.stdin.readline

n, m=map(int, input().split())
five=[5, 25, 125, 625, 3125, 15625, 78125, 390625, 1953125, 9765625, 48828125, 244140625, 1220703125]
two=[2, 4, 8, 16, 32, 64, 128, 256, 512, 1024, 2048, 4096, 8192, 16384, 32768, 65536, 131072, 262144, 524288, 1048576, 2097152, 4194304, 8388608, 16777216, 33554432, 67108864, 134217728, 268435456, 536870912, 1073741824]
cnt5=0
for f in five:
    cnt5+=n//f
    cnt5-=m//f
    cnt5-=(n-m)//f
cnt2=0
for t in two:
    cnt2+=n//t
    cnt2-=m//t
    cnt2-=(n-m)//t

print(min(cnt2, cnt5))